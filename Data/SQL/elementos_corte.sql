SELECT C.G3E_FID,  
       E.NODO1_ID, 
       E.NODO2_ID, 
       C.COOR_GPS_LON AS LON, 
       C.COOR_GPS_LAT AS LAT,       
       E.CIRCUITO, 
       C.CODIGO_OPERATIVO, 
       C.G3E_FNO, 
       E.EST_ESTABLE,
       CASE 
           WHEN C.G3E_FNO = 19300 THEN 'S'
           WHEN C.G3E_FNO = 19400 THEN 'C'
           WHEN C.G3E_FNO = 19800 THEN 'R'
           WHEN C.G3E_FNO = 18800 THEN 'I'
           WHEN C.G3E_FNO = 19700 THEN 'P'
           ELSE NULL
       END AS TIPO
FROM CCONECTIVIDAD_E E, CCOMUN C
WHERE E.G3E_FID = C.G3E_FID 
  AND C.EMPRESA_ORIGEN = 'EDEQ' 
  AND C.ESTADO = 'OPERACION' 
  AND C.G3E_FNO IN (19300, 19400, 19800, 18800, 19700)
ORDER BY G3E_FNO;