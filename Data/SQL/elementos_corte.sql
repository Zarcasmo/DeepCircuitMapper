SELECT C.G3E_FID,  
       E.NODO1_ID, 
       E.NODO2_ID, 
       C.CODIGO_OPERATIVO,    
       E.CIRCUITO, 
       D.PROPIETARIO_1 AS PROPIETARIO,
       C.MUNICIPIO,
       C.UBICACION,
       C.G3E_FNO, 
       E.EST_ESTABLE,
       E.CAPACIDAD_NOMINAL,
       E.FASES,
       E.TENSION,
       C.FECHA_INSTALACION,
       C.COOR_GPS_LON, 
       C.COOR_GPS_LAT, 
       CASE 
           WHEN C.G3E_FNO = 19300 THEN 'S'
           WHEN C.G3E_FNO = 19400 THEN 'C'
           WHEN C.G3E_FNO = 19800 THEN 'R'
           WHEN C.G3E_FNO = 18800 THEN 'I'
           WHEN C.G3E_FNO = 19700 THEN 'P'
           ELSE NULL
       END AS TIPO
FROM CCONECTIVIDAD_E E, CCOMUN C, CPROPIETARIO D
WHERE E.G3E_FID = C.G3E_FID AND C.G3E_FID = D.G3E_FID
  AND C.EMPRESA_ORIGEN = 'EDEQ' 
  AND C.ESTADO = 'OPERACION' 
  AND C.G3E_FNO IN (19300, 19400, 19800, 18800, 19700)
ORDER BY 
    CASE 
        WHEN C.G3E_FNO = 18800 THEN 1  -- 'I'
        WHEN C.G3E_FNO = 19700 THEN 2  -- 'P'
        WHEN C.G3E_FNO = 19800 THEN 3  -- 'R'
        WHEN C.G3E_FNO = 19400 THEN 4  -- 'C'
        WHEN C.G3E_FNO = 19300 THEN 5  -- 'S'
        ELSE 6
    END

