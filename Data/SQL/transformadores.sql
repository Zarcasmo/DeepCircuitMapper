SELECT  
    T.G3E_FID,
    D.NODO_TRANSFORM_V AS CODIGO, 
    D.CIRCUITO,
    D.NODO1_ID, 
    D.NODO2_ID, 
    D.CAPACIDAD_NOMINAL,
    D.FASES,
    D.TENSION,
    E.PROPIETARIO_1 AS PROPIETARIO,
    C.FECHA_INSTALACION,
    NVL(C.TIPO_REMUNERACION, 'USO') AS TIPO_REMUNERACION,
    C.MUNICIPIO,
    C.UBICACION,
    C.COOR_GPS_LON,
    C.COOR_GPS_LAT       
FROM ETRANSFO_AT T, CCOMUN C, CCONECTIVIDAD_E D,CPROPIETARIO E
WHERE T.G3E_FID = C.G3E_FID AND T.G3E_FID = D.G3E_FID AND T.G3E_FID = E.G3E_FID AND  C.G3E_FID IN (SELECT G3E_FID FROM CCOMUN WHERE G3E_FNO =20400 AND EMPRESA_ORIGEN = 'EDEQ' AND ESTADO = 'OPERACION')
