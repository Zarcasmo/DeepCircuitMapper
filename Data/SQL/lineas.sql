SELECT 
    C.G3E_FID, 
    E.CIRCUITO, 
    E.TENSION, 
    E.NODO1_ID, 
    E.NODO2_ID, 
    E.LONGITUD,
    A.CODE_CONDUCTOR as CONDUCTOR,
    E.FASES,
    E.LOCALIZACION,
    D.PROPIETARIO_1 AS PROPIETARIO,
    C.FECHA_INSTALACION,
    GETORDINATEVALUE(L.G3E_GEOMETRY, 1) AS LON1_1, 
    GETORDINATEVALUE(L.G3E_GEOMETRY, 2) AS LAT1_1, 
    GETORDINATEVALUE(L.G3E_GEOMETRY, 4) AS LON2_2, 
    GETORDINATEVALUE(L.G3E_GEOMETRY, 5) AS LAT2_2  
FROM 
    CCOMUN C 
JOIN 
    CCONECTIVIDAD_E E ON C.G3E_FID = E.G3E_FID
JOIN 
    ECON_PRI_AT A ON C.G3E_FID = A.G3E_FID
JOIN 
    ECON_PRI_LN L ON C.G3E_FID = L.G3E_FID  
JOIN 
    CPROPIETARIO D ON C.G3E_FID=D.G3E_FID
WHERE 
    C.G3E_FNO = 19000 
    AND C.EMPRESA_ORIGEN = 'EDEQ' 
    AND C.ESTADO = 'OPERACION' 
  --  AND (E.TENSION = '13.2' OR E.TENSION = '33')
    AND L.G3E_FID IN (SELECT G3E_FID FROM CCONECTIVIDAD_E WHERE G3E_FNO = 19000)